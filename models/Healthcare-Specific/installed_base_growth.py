
"""
TOP 10 HEALTHCARE / LIFE SCIENCE TOOLS METRICS

1. R&D Intensity (% of Revenue)
   Formula: R&D Expense / Total Revenue
   Measures: Commitment to innovation and platform development relative to sales.

2. R&D Productivity
   Formula: Total Revenue / R&D Expense
   Measures: How effectively the company converts R&D dollars into commercial output.

3. Gross Margin (%)
   Formula: Gross Profit / Total Revenue
   Measures: Pricing power, product mix quality, and competitive moat strength.

4. Gross Margin Stability
   Formula: Standard deviation of Gross Margin over last N periods
   Measures: Consistency of margin performance; lower volatility indicates stable business quality.

5. R&D Runway (Years)
   Formula: (Cash + Short-Term Investments) / Annual R&D Expense
   Measures: How long the company can sustain its innovation spend using current liquidity.

6. Intangible Asset Intensity
   Formula: (Goodwill + Intangible Assets) / Total Assets
   Measures: Degree to which the balance sheet is driven by IP, brand, and acquisitions.

7. Capex Intensity (% of Revenue)
   Formula: Capital Expenditures / Total Revenue
   Measures: Scale of reinvestment in physical capacity relative to revenue.

8. EV / Sales
   Formula: Enterprise Value / Total Revenue (TTM or FY)
   Measures: Valuation relative to top-line performance; widely used in tools & diagnostics.

9. EV / R&D
   Formula: Enterprise Value / R&D Expense
   Measures: Market value placed on each dollar of innovation spending.

10. Net Debt / EBITDA (or Operating Income if EBITDA unavailable)
    Formula: (Total Debt – Cash – Short-Term Investments) / EBITDA
    Measures: Balance sheet leverage and financial risk profile.
"""